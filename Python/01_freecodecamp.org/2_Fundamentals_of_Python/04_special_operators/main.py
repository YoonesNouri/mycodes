# در پایتون تعدادی عملگر وجود دارد که در زبان‌های برنامه‌نویسی Go و Dart یافت نمی‌شوند یا معادل مستقیمی ندارند. این تفاوت‌ها می‌توانند ناشی از ویژگی‌ها و نوع داده‌های منحصر به فرد پایتون باشند. در زیر تعدادی از این عملگرها و توضیحات مربوط به آنها آورده شده است:

# 1. عملگرهای قدرت (Exponentiation Operator)
# پایتون دارای عملگر ** برای محاسبه توان (exponentiation) است که در Go و Dart معادل مستقیمی ندارد.

result = 2 ** 3  # خروجی: 8
__________________________________________________________________________

# 2. عملگرهای تعیین عضویت (Membership Operators)
# عملگرهای in و not in برای بررسی وجود یک عنصر در یک مجموعه (مثل لیست، تاپل، دیکشنری، یا مجموعه) استفاده می‌شوند. Go و Dart این عملگرها را به صورت مستقیم ندارند.

my_list = [1, 2, 3, 4, 5]
print(3 in my_list)  # خروجی: True
print(6 not in my_list)  # خروجی: True
__________________________________________________________________________

# 3. عملگرهای هویت (Identity Operators)
# عملگرهای is و is not برای بررسی هویت اشیاء (یعنی آیا دو شیء به یک مکان در حافظه اشاره دارند یا نه) استفاده می‌شوند. این عملگرها در Go و Dart وجود ندارند.

a = [1, 2, 3]
b = a
c = [1, 2, 3]

print(a is b)  # خروجی: True
print(a is c)  # خروجی: False
__________________________________________________________________________

# 4. عملگرهای تفکیک بیت (Bitwise Operators)
# پایتون دارای عملگرهای ~ (NOT بیت‌به‌بیت)، << (شیفت چپ)، و >> (شیفت راست) است که Go و Dart به طور کامل این عملگرها را ندارند.

a = 0b0011  # 3 in binary
b = 0b0101  # 5 in binary

print(a & b)  # AND بیت‌به‌بیت: 1 (0b0001)
print(a | b)  # OR بیت‌به‌بیت: 7 (0b0111)
print(a ^ b)  # XOR بیت‌به‌بیت: 6 (0b0110)
print(~a)     # NOT بیت‌به‌بیت: -4 (0b1100)
print(a << 2) # شیفت چپ: 12 (0b1100)
print(a >> 2) # شیفت راست: 0 (0b0000)
__________________________________________________________________________

# 5. عملگرهای تخصیص ترکیبی (Compound Assignment Operators)
# پایتون عملگرهای تخصیص ترکیبی مانند +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<= دارد. برخی از این عملگرها در Go و Dart وجود ندارند یا معادل‌های متفاوتی دارند.

x = 5
x **= 3  # معادل: x = x ** 3
print(x)  # خروجی: 125
__________________________________________________________________________

# 6. عملگر تکرار (Repetition Operator)
# پایتون دارای عملگر * برای تکرار دنباله‌ها (مانند لیست‌ها و رشته‌ها) است که در Go و Dart وجود ندارد.

my_list = [1, 2, 3]
print(my_list * 3)  # خروجی: [1, 2, 3, 1, 2, 3, 1, 2, 3]

my_string = "Hello "
print(my_string * 3)  # خروجی: Hello Hello Hello 
__________________________________________________________________________

# 7. عملگر تخصیص تک عنصر (Single Element Assignment)
# پایتون اجازه می‌دهد که متغیرها را به صورت تک‌عنصر اختصاص داد که معادل مستقیمی در Go و Dart ندارد.

a, b, c = 1, 2, 3
print(a, b, c)  # خروجی: 1 2 3
__________________________________________________________________________

# در پایتون، عملگر // برای انجام تقسیم صحیح به کار می‌رود. این عملگر نتیجه تقسیم دو عدد را به صورت عدد صحیح (بدون قسمت اعشاری) برمی‌گرداند.
# مثال پایتون
result = 7 // 2
print(result)  # خروجی: 3
