# شرط‌های if و else
# اگر شرط if درست باشد، بلوک کد مربوطه اجرا می‌شود. در غیر این صورت، بلوک کد else اجرا می‌شود.

x = 10

if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")
    
_____________________________________________________________________________________________________

# شرط‌های if، elif و else
# شما می‌توانید چندین شرط را با استفاده از elif بررسی کنید.

x = 10

if x > 15:
    print("x is greater than 15")
elif x > 5:
    print("x is greater than 5 but less than or equal to 15")
else:
    print("x is 5 or less")
    
_____________________________________________________________________________________________________
    
# ترکیب شرط‌ها با and و or
# شما می‌توانید شرط‌ها را با استفاده از عملگرهای منطقی and و or ترکیب کنید.

x = 10
y = 20

if x > 5 and y > 15:
    print("Both conditions are true")

if x > 15 or y > 15:
    print("At least one of the conditions is true")
    
_____________________________________________________________________________________________________

# شرط‌های تو در تو (Nested if statements)
# شما می‌توانید شرط‌ها را داخل یکدیگر قرار دهید.

x = 10

if x > 5:
    print("x is greater than 5")
    if x < 15:
        print("x is also less than 15")
        
_____________________________________________________________________________________________________

# استفاده از شرط‌ها به صورت فشرده (Ternary Operator)
# پایتون از عبارت شرطی فشرده نیز پشتیبانی می‌کند.

x = 10
result = "x is greater than 5" if x > 5 else "x is not greater than 5"
print(result)

