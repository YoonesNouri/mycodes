//آرگومان های یک فانکشنی که defer شده،
//زمانی ارزیابی می شوند که عبارت defer ارزیابی شود.
//در مثال زیر، عبارت "i" زمانی ارزیابی می شود که کال Println به تعویق افتاده باشد.
//کالِ defer شده، "0" را پس از اینکه فانکشن return کرد پرینت می کند.
//لذا می بینیم که i++ عمل نمیکند و مقدار i برابر با 1 نمی شود.
package main

import "fmt"

func main() {
	a()
}
func a() {
	i := 0
	defer fmt.Println(i)
	i++
	return
}
